package base;

public class Operator {
    public static void main(String[] args){
        int a = 8;
        int b = 9;
        System.out.println(a+b);

        System.out.println(a>b);

        System.out.println(a++);

        System.out.println(a);

        // 幂运算
        double c= Math.pow(a,b);
        System.out.println(c);

    }
}
